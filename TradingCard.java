
public class TradingCard {
	private String name;
	private String image;
	
	public TradingCard(String name, String image) {
		this.name = name;
		this.image = image;
	}
	
	public String getName() {
		return name;
	}
	
	public String getImg() {
		return image;
	}
	
	public String toString() {
		return name;
	}

}
